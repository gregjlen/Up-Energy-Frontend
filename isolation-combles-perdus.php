                            <h5 class="entry-heading">Diagnostic : Quand (re)faire l'isolation des combles perdus ?</h5>
                            <p class="entry-desc">
                                <b>Isolant mouillé</b><br><br>

                                Si votre isolant est de type « laine minérale » (laine de verre ou laine de roche), vérifiez qu’il est bien sec car ses performances se dégradent de manière irréversible en présence d'eau. La présence d'humidité doit être identifiée avant d’être traitée. Elle peut venir de l'extérieur (intempéries, fuite d'un tuyau...) ou de l'intérieur (risque de condensation en l’absence de pare-vapeur).
                                <br><br>
                                <b>Laine de verre mouillée</b><br><br>

                                Si la laine minérale est mouillée et que les conditions de séchage ne sont pas réunies, il faut envisager un renouvellement.
                                <br><br>
                                <b>Tassement</b><br><br>

                                Les laines minérales peuvent se tasser sous leur propre poids, ce qui diminue leur épaisseur et leurs performances, surtout si l’épaisseur posée initialement était faible. Une isolation des combles de moins de 15 cm doit vous alerter. Pour rappel, la durée de vie d'une isolation de combles est de 25-30 ans environ.

                                <br><br>
                                <b>Phénomène de vagues</b><br><br>

                                Si votre isolation a été mal posée, elle peut présenter des vagues. Les rouleaux de laine minérale ont été déposés directement sur les lambourdes sans aucune découpe. Il se crée alors un effet de circulation d'air en sous face ce qui diminue considérablement l'efficacité de l'isolation.

                                <br><br>
                                <b>Isolation vagues</b><br><br>

                                Si vous n'avez pas accès à vos combles, il existe des dispositifs à distance comme la caméra thermique par exemple. Attention cependant si vous louez vous-même ce matériel, car son utilisation et l'interprétation des résultats sont délicates. Pour des mesures optimales, il faut utiliser la caméra thermique pendant la saison de chauffe (radiateurs allumés) et lorsqu'il fait froid dehors (moins de 5°C). Pour cibler au mieux les économies d'énergie possibles, vous pouvez vous diriger vers un bureau d'études thermiques qui sera plus qualifié pour mener cette opération.




