                            <p class="entry-desc">

                                Après le toit, les murs sont la seconde source de déperdition de chaleur dans les logements. C’est ici que se produisent 20 à 25 % des pertes thermiques dans une maison ancienne mal isolée (source : Ademe). L’isolation thermique des murs par l’extérieur (ITE) réduit donc sensiblement ce phénomène.

                                <h6>Un manteau protecteur</h6>

                                picto-isolation-murs-maisonLa technique de l’ITE consiste à envelopper le bâtiment d’une couche totalement hermétique. Cette « seconde peau » limite durablement la consommation d’énergie. À la clé, une amélioration des performances thermiques comme acoustiques.

                                <h6>La surface habitable préservée</h6>
                                Contrairement à l’isolation par l’intérieur (ou ITI), l’ITE ne grignote pas les mètres carrés. La surface habitable est ainsi préservée. Un avantage majeur, notamment pour les plus petits logements.
                                Que choisir entre isolation thermique par l'extérieur et isolation thermique par l'intérieur ? Consultez également le comparatif entre ITE et ITI par Hellio.

                                Ò<h6>Un chantier peu invasif</h6>
                                Autre atout, cette rénovation énergétique s’effectue uniquement à l’extérieur. Dès lors, les pièces intérieures ne sont pas condamnées pendant le chantier. Les occupants peuvent continuer à vivre normalement, si ce n'est l'accoutumance au échafaudages devant les fenêtres.

                                <h6>Une seconde jeunesse pour la façade</h6>
                                Cette opération est aussi l’occasion de changer l’aspect de votre habitat. Selon la technique choisie, vos murs profitent en effet d’un nouveau bardage ou d’un nouvel enduit de ravalement. Une transformation esthétique qui valorise votre logement, surtout dans le cas d'une maison ancienne qui aurait besoin d'un petit lifting.



                                Dans la grande majorité des cas, l’isolation du sol s’envisage dans les projets en construction pendant la phase du chantier. Dans ces cas, l’isolation fera la durée de vie de la maison. Refaire l’isolation du sol nécessite de le casser en premier lieu, il est donc primordial de prévoir une isolation de qualité lors de la construction de la maison.
                                <br><br>
